# Take two numbers and print their sum, difference, and product.

def main():
    a = float(input("Enter first number: "))
    b = float(input("Enter second number: "))
    print(f"Sum: {a + b}")
    print(f"Difference: {a - b}")
    print(f"Product: {a * b}")


if __name__ == "__main__":
    main()
